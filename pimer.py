# 1. Computer Programming
# Computer programming is the process of writing instructions (code) that a computer can understand and execute to perform specific tasks.

# 2. Memory Partitioning
# Memory partitioning is the division of a computer's memory into sections so that different programs or processes can use memory efficiently.

# 3. Memory Address
# A memory address is a unique number that identifies a specific location in the computer's memory where data is stored.

# 4. How RAM connects the CPU and Hard Disk
# RAM acts as a temporary working space between the CPU and the hard disk. Data is loaded from the hard disk into RAM, and the CPU accesses it from RAM because RAM is much faster than the hard disk.

# 5. What is a Variable
# A variable is a named storage location in memory used to store data that can be changed during program execution.

# 6. What is a Statement
# A statement is a single line of code that performs a specific action, such as assigning a value or printing output.

# 7. What is Code
# Code is a set of instructions written in a programming language that tells a computer what to do.

# 8. What is a Block of Code
# A block of code is a group of related statements that are executed together. In Python, blocks are defined using indentation.

# 9. What is a Comment
# A comment is a note in the code that is ignored by the computer and is used to explain or document the code for humans.

# 10. List any 4 syntax of variables
# Examples of variable syntax in Python:
x = 10              # integer variable
name = "Mercy"      # string variable
height = 5.6        # float variable
is_student = True   # boolean variable

# 11. Declaring and Initialising
# Declaring a variable means creating it (naming it).
# Initialising a variable means assigning it a value. Example:
age = 25  # 'age' is declared and initialised with value 25